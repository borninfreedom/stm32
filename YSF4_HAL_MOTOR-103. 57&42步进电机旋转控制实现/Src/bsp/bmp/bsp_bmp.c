void LCD_Fill_Pic(uint16_t x, uint16_t y,uint16_t pic_H, uint16_t pic_V, const unsigned char* pic)
{
  unsigned long i;
  
	LCD_OpenWindow(x,y,pic_H,pic_V);
  /* 开始向GRAM写入数据 */
	LCD_WRITE_CMD ( 0x2C );	
  
	for (i = 0; i < pic_H*pic_V*2; i +=2)
	{
		LCD_WRITE_DATA((pic[i]<<8)|pic[i+1]);
	}
}
